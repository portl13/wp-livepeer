    <h3>3. Create a Stream</h3>
    <p>Once you have obtained an API key, follow the instructions on the <a target="_blank" href="<?php echo site_url()?>/wp-admin/admin.php?page=livepeer-options">Livepeer WP options</a> page to get setup your WordPress Livepeer stream.</p>

    <h3>4. Create an Event</h3>
    <p><a target="_blank" href="<?php echo site_url()?>/wp-admin/edit.php?post_type=livepeer-events">Livepeer WP Events</a> are like a digital flyer to pass around to your friends on your socials! Add a Cover Image, Date & Time, and name the Event as well as a link to the channel page for your viewers to watch your LIVE stream.</p>
